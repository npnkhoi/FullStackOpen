const typeDefs = gql`
  type Book {
    title: String!
    published: Int
    author: String!
    genres: [String]
  }
  type Author {
    name: String!
    born: Int
    bookCount: Int!
  }
  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(name: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }
  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Author: {
    bookCount: (root) => books.filter(book => book.author === root.name).length
  },
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allAuthors: () => authors,
    allBooks: (root, args) => {
      let ret = books
      if (args.name) {
        ret = ret.filter(book => book.author === args.name)
      }
      if (args.genre) {
        ret = ret.filter(book => book.genres.includes(args.genre))
      }
      return ret
    }
  },
  Mutation: {
    addBook: (root, args) => {
      const {title, author, published, genres} = args
      const newBook = {title, author, published, genres}
      let isNew = false
      books = books.map(book => {
        if (book.title == title) {
          isNew = true
          return newBook
        } else {
          return book
        }
      })
      if (!isNew) {
        books = books.concat(newBook)
      }
      if (authors.filter(aut => aut.name === author).length === 0) {
        authors = authors.concat({name: author})
      }
      return newBook
    },
    editAuthor: (root, {name, setBornTo}) => {
      let foundAuthor = null
      authors = authors.map(author => {
        if (author.name == name) {
          author.born = setBornTo
          foundAuthor = author
        }
        return author
      })
      return foundAuthor
    }
  } 
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})